export function detectInitialLanguage() {
  let saved = null
  try {
    saved = window.localStorage.getItem('portfolio-language')
  } catch {
    // Language detection still works when browser storage is unavailable.
  }
  if (languageOptions.some(({ key }) => key === saved)) return saved

  const languages = navigator.languages?.length ? navigator.languages : [navigator.language]
  for (const language of languages) {
    const locale = language.toLowerCase()
    if (locale.startsWith('ja')) return 'ja'
    if (
      locale.startsWith('zh-hant') ||
      locale.startsWith('zh-tw') ||
      locale.startsWith('zh-hk') ||
      locale.startsWith('zh-mo')
    ) return 'zh-Hant'
    if (locale.startsWith('zh')) return 'zh'
  }
  return 'en'
}

function mergeLocale(base, translation) {
  return {
    ...base,
    ...translation,
    projects: base.projects.map((project) => ({
      ...project,
      ...translation.projects.find((item) => item.id === project.id),
    })),
    commercial: Object.fromEntries(
      Object.entries(base.commercial).map(([key, item]) => [
        key,
        { ...item, ...translation.commercial[key] },
      ]),
    ),
  }
}

